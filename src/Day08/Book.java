package Day08;

public class Book { // Book class start
    String title;
    String author;
    int price;
    // Book 클래스에 title, author, price를 매개변수로 받아 멤버 변수를 초기화하는 생성자를 만드세요.
    Book( String title, String author, int price ){
        this.title = title;
        this.author = author;
        this.price = price;
    }
} // Book class end
